#include "vkApplication.hpp"

/* ========================================================================== */
/*                                     Run                                    */
/* ========================================================================== */
void vkApplication::Run()
{
    vkWindow vkwnd();
}


/* ========================================================================== */
/*                                  MainLoop                                  */
/* ========================================================================== */
void vkApplication::MainLoop()
{
    while (!glfwWindowShouldClose(vkWnd.getWindow())) 
    {
        glfwPollEvents();
    }
}